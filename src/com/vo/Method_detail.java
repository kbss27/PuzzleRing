package com.vo;

public class Method_detail {
	private String method_am, method_type, method_modifier, method_name, class_name;

	public String getMethod_am() {
		return method_am;
	}

	public void setMethod_am(String method_am) {
		this.method_am = method_am;
	}

	public String getMethod_type() {
		return method_type;
	}

	public void setMethod_type(String method_type) {
		this.method_type = method_type;
	}

	public String getMethod_modifier() {
		return method_modifier;
	}

	public void setMethod_modifier(String method_modifier) {
		this.method_modifier = method_modifier;
	}

	public String getMethod_name() {
		return method_name;
	}

	public void setMethod_name(String method_name) {
		this.method_name = method_name;
	}

	public String getClass_name() {
		return class_name;
	}

	public void setClass_name(String class_name) {
		this.class_name = class_name;
	}
}
